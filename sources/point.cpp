/**************************************************************************\
*                                                                          *
*  opérateur +=                                                            *
*                                                                          *
\**************************************************************************/
point& point::operator+=(const point& p)
{
  x += p.x;
  y += p.y;
  z += p.z;

  return *this;
}




/**************************************************************************\
*                                                                          *
*  opérateur -=                                                            *
*                                                                          *
\**************************************************************************/
point& point::operator-=(const point& p)
{
  x -= p.x;
  y -= p.y;
  z -= p.z;

  return *this;
}




/**************************************************************************\
*                                                                          *
*  opérateur *=                                                            *
*                                                                          *
\**************************************************************************/
point& point::operator*=(const float f)
{
  x *= f;
  y *= f;
  z *= f;

  return *this;
}




/**************************************************************************\
*                                                                          *
*  opérateur /=                                                            *
*                                                                          *
\**************************************************************************/
point& point::operator/=(const float f)
{
  x /= f;
  y /= f;
  z /= f;

  return *this;
}





